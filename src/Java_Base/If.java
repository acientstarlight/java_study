package Java_Base;

/**
 * If条件语句
 */

public class If {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        if(a==b){
            System.out.println("a和b相等");
        }else if (a>b){
            System.out.println("a大于等于b");
        }else {
            System.out.println("a小于b");
        }

    }
}
